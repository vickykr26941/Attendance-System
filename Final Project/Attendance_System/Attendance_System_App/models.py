@receiver(post_save, sender=CustomUser)
def create_user_profile(sender, instance, created, **kwargs):
    if created:
        if instance.user_type == 1:
            Admin.objects.create(admin=instance)
        if instance.user_type == 2:
            Teachers.objects.create(admin=instance, address="")
        if instance.user_type == 3:
            StudentServiceStaffs.objects.create(admin=instance, address="")
        if instance.user_type == 4:
            Students.objects.create(admin=instance, course_id=Courses.objects.get(id=1),
                                    session_year_id=SessionYearModel.object.get(id=1), address="", profile_pic="",
                                    gender="")


@receiver(post_save, sender=CustomUser)
def save_user_profile(sender, instance, **kwargs):
    if instance.user_type == 1:
        instance.admin.save()
    if instance.user_type == 2:
        instance.teachers.save()
    if instance.user_type == 3:
        instance.studentservicestaffs.save()
    if instance.user_type == 4:
        instance.students.save()
